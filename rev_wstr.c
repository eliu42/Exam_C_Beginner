#include <unistd.h>

int		main(int argc, char **argv)
{
	int		i;
	int		j;

	i = 0;
	while (argc == 2 && argv[1][i])
		i++;
	while (argc == 2 && i > 0)
	{
		while (argv[1][i] != ' ' && i != 0)
			i--;
		j = i;
		if (j)
			j++;
		while (argv[1][j] && argv[1][j] != ' ')
			write(1, &argv[1][j++], 1);
		if (i--)
			write(1, " ", 1);
	}
	write(1, "\n", 1);
	return (0);
}
